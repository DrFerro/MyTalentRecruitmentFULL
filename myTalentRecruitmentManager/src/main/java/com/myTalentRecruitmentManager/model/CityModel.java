package com.myTalentRecruitmentManager.model;

public class CityModel {

	private long id;
	private String name;

	public CityModel() {}
	
	public long getId() {
		return id;
	}
	public void setId(long id) {
		this.id = id;
	}
	
	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
	
}
